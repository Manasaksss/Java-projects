package aa.mm;

public class even {
	public void printEvenNumbers() {
        for (int i = 0; i <= 20; i += 2) {
            System.out.println("Even: " + i);
        }
    }

    public void printOddNumbers() {
        for (int i = 1; i <= 20; i += 2) {
            System.out.println("Odd: " + i);
        }
    }
}

